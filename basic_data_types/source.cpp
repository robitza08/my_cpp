#include <iostream>

using namespace std;

int main()
{
	char c = 'a';
	cout << "\n\nThe value of variable c (type char) is: " << c;
	int i = 23;
	cout << "\nThe value of variable i (type integer) is: " << i;
	bool b = true;
	cout << "\nThe value of variable b (type boolean) is: " << b;
	double d = 123.45;
	cout << "\nThe value of variable d (type double) is: " << d;
	cout << "\n\n";
}